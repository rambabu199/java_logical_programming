class Arthemeticoperations {
  public static void main(String args[]) {
    int a = 100, b = 20, c;
    System.out.println("a value is" + a);
    System.out.println("b value is" + b);
    c = a + b;
    System.out.println("c value is" + c);
    c = a - b;
    System.out.println("c value is" + c);
    c = a * b;
    System.out.println("c value is" + c);
    c = a / b;
    System.out.println("c value is" + c);
    c = a % b;
    System.out.println("c value is" + c);
  }
}